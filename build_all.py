def rp_main(html_path, new_content):
    with open(html_path, 'r', encoding='utf-8') as f:
        c = f.read()
    s = c.find('<main id="main">')
    e = c.find('</main>')
    if s < 0 or e < 0:
        print('ERR: no main in ' + os.path.basename(html_path))
        return False
    nc = c[:s] + '<main id="main">\n\n' + new_content + '\n\n</main>' + c[e+7:]
    with open(html_path, 'w', encoding='utf-8') as f:
        f.write(nc)
    sz = len(nc)
    print('DONE: ' + os.path.basename(html_path) + ' (' + str(sz) + ' chars)')
    return True

Q = chr(39)

def mkcard(cid, num, title, grp, platforms, cta, summary, tl, batch, img=None):
    ph = ''.join(
        '<span class="ptag ptag-{0}">{1}</span>'.format(
            'ig' if 'IG' in p else 'tk' if 'TikTok' in p else 'fb' if 'FB' in p else 'th' if 'Threads' in p else 'xh',
            p)
        for p in platforms)
    th = ''.join(
        '            <div class="ts-row"><div class="ts-time">{0}</div><div class="ts-desc">{1}</div></div>\n'.format(t, d)
        for t, d in tl)
    ih = '        <div class="card-image-section"><img src="{0}" style="max-width:100%;"></div>\n'.format(img) if img else ''
    btn_shot = 'toggleShot(' + Q + cid + Q + ',this)'
    btn_open = 'toggleDetail(' + Q + cid + '-d' + Q + ',this)'
    return (
        '      <div class="script-card" data-tags="' + grp + ' ' + cta + '" data-id="' + cid + '">\n'
        '        <div class="card-top">\n'
        '          <div class="card-num">' + num + '</div>\n'
        '          <div class="card-title-block">\n'
        '            <div class="card-title">' + title + '</div>\n'
        '            <div class="card-meta"><span>60 秒</span><span>' + batch + '</span><span>' + cta + '</span></div>\n'
        '          </div>\n'
        '        </div>\n'
        '        <div class="card-summary">' + summary + '</div>\n'
        '        <div class="card-footer">'
        '<div class="platform-tags">' + ph + '</div>'
        '<div class="card-actions">'
        '<button class="btn-shot" onclick="' + btn_shot + '">已拍</button>'
        '<button class="btn-open" onclick="' + btn_open + '">展開</button>'
        '</div></div>\n'
        '        <div class="card-detail" id="' + cid + '-d">\n'
        '          <div class="det-label">時間軸</div>\n'
        '          <div style="margin-bottom:14px">\n' +
        th +
        '          </div>\n' + ih +
        '        </div>\n'
        '      </div>'
    )

def mkgroup(name, color, gid, cards):
    inner = '\n'.join(cards)
    cnt = str(len(cards))
    return (
        '  <div class="group" data-group="' + name + '">\n'
        '    <div class="group-header collapsed" onclick="toggleGroup(this)">\n'
        '      <div class="group-header-left">\n'
        '        <div class="group-dot" style="background:' + color + '"></div>\n'
        '        <span class="group-title">' + name + '</span>\n'
        '        <span class="group-count" id="gc-' + str(gid) + '">' + cnt + ' 部</span>\n'
        '      </div>\n'
        '      <span class="group-toggle">&#9662;</span>\n'
        '    </div>\n'
        '    <div class="group-body hidden" id="gb-' + str(gid) + '">\n\n' +
        inner + '\n\n'
        '    </div>\n'
        '  </div>'
    )

