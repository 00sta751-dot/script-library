def sc_article(num, title, grp_label,派系, cta, platforms, scene, timeline):
    aid = str(num).zfill(2)
    pl_tags = ''.join('<span class="tag">' + p + '</span>' for p in platforms)
    派系_tags = ''.join('<span class="tag">' + d + '</span>' for d in 派系)
    cta_tag = '<span class="tag pe">' + cta + '</span>'
    tl_html = ''
    for ts, desc, *rest in timeline:
        sub = rest[0] if rest else ''
        tl_html += (
            '<div class="sl"><span class="st">' + ts + '</span>'
            '<p class="sd">' + desc + '</p>'
        )
        if sub:
            tl_html += '<p class="sc-sub">' + sub + '</p>'
        tl_html += '</div>\n'
    return (
        '<!-- #' + aid + ' -->\n'
        '<article class="sc" data-id="' + aid + '">\n'
        '<div class="sh" onclick="s(this)">\n'
        '  <div class="tp">\n'
        '    <span class="nm">No.' + aid + '</span>\n'
        '    <div class="ac"><button class="db" onclick="d(event,this)">已拍</button></div>\n'
        '  </div>\n'
        '  <div class="tg">\n'
        + 派系_tags + cta_tag + pl_tags +
        '\n  </div>\n'
        '  <h2 class="ti">' + title + '</h2>\n'
        '  <p class="vi">' + scene + '</p>\n'
        '  <p class="ht">&#9660; Open</p>\n'
        '</div>\n'
        '<div class="sb-body">\n'
        '  <div class="sg">\n' +
        tl_html +
        '  </div>\n'
        '  <div class="batch-tag">' + BATCH + '</div>\n'
        '</div>\n'
        '</article>'
    )

