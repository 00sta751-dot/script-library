def rux_article(num, title, pie, insight, scene, timeline, cta, img=None):
    pid = 'c' + str(num)
    color = PIE_COLORS.get(pie, '#444')
    Q = chr(39)
    tl_html = ''
    for ts, say, sub, *rest in timeline:
        mirror = rest[0] if rest else ''
        tl_html += '        <div class="row"><div class="time">' + ts + '</div><div class="say">' + say + '</div>'
        if mirror:
            tl_html += '<div class="mirror">藏鏡人　' + Q + mirror + Q + '</div>'
        if sub:
            tl_html += '<div class="sub">' + sub + '</div>'
        tl_html += '</div>\n'
    img_html = ''
    if img:
        img_html = '    <div class="ref-link">釣魚部 · 圖卡附件：<a href="' + img + '">' + img + '</a></div>\n'
    return (
        '<article class="card" data-cat="' + pie + '" id="' + pid + '">\n'
        '  <div class="card-head" style="--pie:' + color + '">\n'
        '    <div class="card-meta">\n'
        '      <button class="shot-toggle" type="button" aria-label="切換已拍過">已拍過</button>\n'
        '      <span class="pie">' + pie + '</span>\n'
        '      <span class="num">No. ' + str(num).zfill(2) + '</span>\n'
        '      <span class="batch">' + BATCH + '</span>\n'
        '    </div>\n'
        '    <h3 class="title">' + title + '</h3>\n'
        '    <div class="insight">' + insight + '</div>\n'
        '  </div>\n'
        '  <div class="card-body">\n'
        '    <div class="scene"><b>場景</b>　' + scene + '</div>\n' +
        img_html +
        '    <div class="timeline">\n' +
        tl_html +
        '    </div>\n'
        '    <div class="cta">\n'
        '      <span class="cta-arrow">→</span>\n'
        '      <span>' + cta + '</span>\n'
        '    </div>\n'
        '  </div>\n'
        '</article>'
    )

def section(roman, label, en, sect_id, cards, count):
    return (
        '<header class="section-head" id="sect-' + str(sect_id) + '">\n'
        '  <span class="roman">' + roman + '</span>\n'
        '  <span class="label">' + label + '<span class="en">' + en + '</span></span>\n'
        '  <span class="rule"></span>\n'
        '  <span class="count">' + str(count) + ' scripts</span>\n'
        '</header>\n'
        '<div class="cards">\n' +
        '\n'.join(cards) + '\n'
        '</div>'
    )

